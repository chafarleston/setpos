    private void init() {
       table = new MyJTable();
       table.setAutoCreateRowSorter(true);
       table.setFillsViewportHeight(true);
       jTextFilter.getDocument().addDocumentListener(new FilterListener());

    }
    
    public void createTable(int row, int col) {
        model = new MyDefaultTableModel(row, col);
        table.setModel(model);
        sorter  = new TableRowSorter<TableModel>(model);
        table.setRowSorter(sorter);
    }
    
    public void createTable(Object[][] data, Object[] header) {
        model = new MyDefaultTableModel(data, header);
        table.setModel(model);
        sorter  = new TableRowSorter<TableModel>(model);
        table.setRowSorter(sorter);
    }
    
    public void addLine(Object[] row){
        model.addRow(row);
    }
    
    public void refresh() {
        jScrollPane1.setViewportView(table);
        //jScrollPane1.revalidate();
        //repaint();
    }
    
    public void clear() {
        createTable(0, 0);
        refresh();
    }
    
    public MyDefaultTableModel getTableModel() {
        return model;
    }
    
    private void newFilter() {
        RowFilter<TableModel, Object> rf = null;

        try {
            rf = RowFilter.regexFilter(jTextFilter.getText(), 1);
        } catch (java.util.regex.PatternSyntaxException e) {
            return;
        }
        sorter.setRowFilter(rf);
    }
    
    public class FilterListener implements DocumentListener {

        @Override
        public void insertUpdate(DocumentEvent arg0) {
             newFilter();
        }

        @Override
        public void removeUpdate(DocumentEvent arg0) {
             newFilter();
        }

        @Override
        public void changedUpdate(DocumentEvent arg0) {
             newFilter();
        }
        
    }
    
    public class MyDefaultTableModel extends DefaultTableModel {  
        
        public MyDefaultTableModel(int row, int col) {  
            super(row, col);  
        }
        
        public MyDefaultTableModel(Object[][] content, Object[] header) {  
            super(content, header);  
        }  
        

        @Override
        public boolean isCellEditable(int row, int col) {  
            return false;  
        }
    }
    
    public class MyJTable extends JTable {
        
        public MyJTable() {
            super();
        }
        
        @Override
        public boolean getScrollableTracksViewportWidth() {
   	if (autoResizeMode != AUTO_RESIZE_OFF) {
 	    if (getParent() instanceof JViewport) {
 		return (((JViewport)getParent()).getWidth() > getPreferredSize().width);
 	    }
 	} 
 	return false;
    }

    }
    
